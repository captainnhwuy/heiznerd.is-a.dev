import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const stickersDir = path.join(__dirname, '../public/stickers/Organized_Stickers');
const outputFile = path.join(__dirname, '../src/stickers.js');

function getAllFiles(dirPath, arrayOfFiles) {
    const files = fs.readdirSync(dirPath);

    arrayOfFiles = arrayOfFiles || [];

    files.forEach(function (file) {
        if (fs.statSync(dirPath + "/" + file).isDirectory()) {
            arrayOfFiles = getAllFiles(dirPath + "/" + file, arrayOfFiles);
        } else {
            if (file.match(/\.(png|jpg|jpeg|webp)$/i)) {
                // Create public path relative to 'public' folder
                const fullPath = path.join(dirPath, file);
                // Normalize path separators to forward slashes for URL usage
                const relativePath = fullPath.split('public')[1].replace(/\\/g, '/');

                // Extract character name from parent directory
                const character = path.basename(path.dirname(fullPath));

                arrayOfFiles.push({
                    path: relativePath,
                    character: character,
                    name: file
                });
            }
        }
    });

    return arrayOfFiles;
}

try {
    console.log('Scanning for stickers...');
    const stickers = getAllFiles(stickersDir);

    // Group by character
    const stickersByChar = {};
    stickers.forEach(s => {
        if (!stickersByChar[s.character]) {
            stickersByChar[s.character] = [];
        }
        stickersByChar[s.character].push(s.path);
    });

    const fileContent = `// Auto-generated by generate-stickers.js
export const stickers = ${JSON.stringify(stickersByChar, null, 2)};

export const allStickerPaths = ${JSON.stringify(stickers.map(s => s.path), null, 2)};
`;

    fs.writeFileSync(outputFile, fileContent);
    console.log(`Success! Found ${stickers.length} stickers from ${Object.keys(stickersByChar).length} characters.`);
    console.log(`Wrote data to ${outputFile}`);

} catch (e) {
    console.error('Error generating sticker data:', e);
}
